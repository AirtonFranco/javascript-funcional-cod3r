//colocando função dentro de uma variavel ou constantes

const add = function (a, b) {
    return a + b
}

const subt = function (a, b) {
    return a - b
}

const multi = (a, b) => {
    return a * b
}

const div = (a, b) => a / b

console.log(add(2, 2))
console.log(subt(2, 2))
console.log(multi(2, 2))
console.log(div(2, 2))